    private void btnDividirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDividirActionPerformed
        int num = Integer.parseInt(txtNum.getText()); //pega o numerador e passa de string pra int
        int den = Integer.parseInt(txtDen.getText()); //o mesmo com o denominador
        int div = num / den; //divis達o inteira
        lblDiv.setText(Integer.toString(div)); //imprimir o resultado de int pra string
        int resto = num % den; //resto da divis達o inteira
        lblResto.setText(Integer.toString(resto)); //o mesmo pro resto da divis達o
    }//GEN-LAST:event_btnDividirActionPerformed

