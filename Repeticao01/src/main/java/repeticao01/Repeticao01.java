public class Repeticao01 {
    
    public static void main(String[] args) {
        int cc = 0;
        //repetição com pós-teste (faça - enquanto ou do - while)
        do{ //imrpima na tela e incremente cc
            System.out.println("Cambalhota " + cc); 
            cc++;
        } while(cc < 4); //enquanto cc for menor que 0
        
    }
    
}
